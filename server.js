dotenv.config({ path: './config.env' });
const app = require('./app'); // need to require app file after env commands

// replace password in connection string
const DB = process.env.DATABASE.replace(
  '<PASSWORD>',
  process.env.DATABASE_PASSWORD
);

mongoose
  .connect(DB, {
    useNewUrlParser: true,
    useCreateIndex: true,
    useFindAndModify: false,
  })
  .then(() => console.log('DB connection successful!')); // access the promis created by connection through using then()

